# Ввод общего количества журавликов
S = int(input())

# Петя и Сережа сделали одинаковое количество:
# Пусть Петя = x, Сережа = x, тогда Катя = 2*(x + x) = 4x
# Всего: x + x + 4x = 6x = S
# Отсюда x = S // 6 (целочисленное деление)

petya = S // 6
serezha = petya
katya = 4 * petya

# Вывод результата
print(petya, katya, serezha)