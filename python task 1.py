# Задача про журавликов
# Петя, Катя и Сережа делают журавликов из бумаги

# Получаем общее количество журавликов
S = int(6)

# Решение:
# Пусть Петя = x, Сережа = x (они сделали одинаково)
# Катя сделала в 2 раза больше, чем Петя и Сережа вместе
# Катя = 2 * (x + x) = 4x
# Всего: x + x + 4x = 6x = S
# Значит: x = S // 6

petya = S // 6
sergey = S // 6
katya = 4 * (S // 6)

# Выводим результат: Петя, Катя, Сережа
print(petya, katya, sergey)